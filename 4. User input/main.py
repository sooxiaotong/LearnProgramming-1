print("Please input your name: ")  # First ask for user input
name = input()  # Reads input from user then puts it in a variable
print("Hello ,", name)  # Outputs user input

print("Please input a number: ")  # Ask user to input number
number = input()  # Reads input from user then puts it in another variable
print("You have entered: ", number)  # Outputs second user input

